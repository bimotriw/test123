package com.oustme.oustsdk.launcher;
/**
 * These are the Configuration that can be applied on Oust App
 * #1. If PLAY_MODE is true the  play mode will be shown in App
 * #2. REWARD_LINK
 * #3. APP_COLOR can be customised
 * #4. LAYOUT_ID can be changed to Grid type or Linear Type
 * #5. SHARE_FEATURE is set to be true if you want to enable share_text of cards, Read More Data, Leaderboard or Assessment Result
 */

public class OustAppConfig {
    public static String PLAY_MODE="playmode";
    public static String REWARD_LINK="reward";
    public static String APP_COLOR="appColor";
    public static String LAYOUT_ID="layoutType";
    public static String PANEL_DISPLAY_NAME ="companydisplayName";
    public static String SHARE_FEATURE="sharefeature";
}
